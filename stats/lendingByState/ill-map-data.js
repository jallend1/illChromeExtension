// ── Pre-aggregated annual totals ──────────────────────────────────────────────
// Built once at load time from MONTHLY so the map can render it without
// iterating over all months on every period switch.

const ALL_2025 = (() => {
  const agg = {};
  for (const mo of Object.values(MONTHLY)) {
    for (const [st, [f, r]] of Object.entries(mo)) {
      if (!agg[st]) agg[st] = [0, 0];
      agg[st][0] += f;
      agg[st][1] += r;
    }
  }
  return agg;
})();
